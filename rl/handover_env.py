import numpy as np
import gymnasium as gym
from gymnasium.spaces import Box, Discrete
from data_models.base_tower import BaseTower
from data_models.car_fcd_data import CarFcdData
from data_models.handover_algorithm import HandoverAlgorithm
from data_models.ng_ran_report import NGRANReport
from data_models.user_equipment import UserEquipment
from helpers.filters import Filters
from utils.fcd_parser import FcdParser
from utils.map_downloader import MapDownloader
from utils.path_gen import PathGeneration
from utils.tower_downloader import TowerDownloader
from utils.wave_utils import WaveUtils


class HandoverEnv(gym.Env):

    def __init__(
        self,
        top_left: float,
        bottom_right: float,
        mcc: int,
        step_len: int,
        simulation_time: float,
    ):
        super().__init__()
        # Prepare Env
        PathGeneration.quick_run(
            skip_netconvert=False,
            step_length=step_len,
            simulation_time=simulation_time,
        )
        MapDownloader.download_osm_by_bbox(
            top_left=top_left,
            bottom_right=bottom_right,
        )
        self.base_towers: list[BaseTower] = TowerDownloader.download_towers_in_bbox(
            top_left=top_left,
            bottom_right=bottom_right,
            mcc=mcc,
        )
        self.fcd_data: list[dict[int, CarFcdData]] = None  # Created on reset()
        self.agent: UserEquipment = None  # Created on reset()
        # Top-4 Filtering
        self.current_top_4: list[BaseTower] = []
        # action space: choosing 1 of 4 BS
        self.action_space = Discrete(4)
        # observation Space
        self.observation_space = Box(low=-1.0, high=1.0, shape=(14,), dtype=np.float32)
        self.step_len = step_len
        self.simulation_time = simulation_time

        self.steps = 0

    def _top_4_towers(self, report: NGRANReport):
        serving = self.agent.serving_bs
        if serving is None:
            return Filters.top_k_towers(
                all_bs=self.base_towers,
                report=report,
                k=4,
            )
        # Top-3 candidates + guaranteed serving slot
        top = Filters.top_k_towers(
            all_bs=self.base_towers,
            report=report,
            k=4,
        )
        if serving in top:
            return top
        # Serving dropped out of top-4: replace the weakest candidate
        top[-1] = serving
        return top

    def _get_obs(self):
        # NOTE: obs is normalized
        if len(self.agent.generated_reports) < 1:
            return np.zeros(self.observation_space.shape, dtype=np.float32)
        # last report generated by the agent
        report: NGRANReport = self.agent.generated_reports[-1]

        # extract RSRP values from the 4 towers, normalize to 0-1
        rsrp_list = [
            WaveUtils.normalize_rsrp_index(
                rsrp_index=report.rsrp_values.get(bs.id, 0),
                radio_type=bs.radio,
            )
            for bs in self.current_top_4
        ]
        # RSRP trend: delta between current and previous report per tower
        if len(self.agent.generated_reports) >= 2:
            prev_report = self.agent.generated_reports[-2]
            rsrp_trend = [
                WaveUtils.normalize_rsrp_index(
                    report.rsrp_values.get(bs.id, 0), bs.radio
                )
                - WaveUtils.normalize_rsrp_index(
                    prev_report.rsrp_values.get(bs.id, 0), bs.radio
                )
                for bs in self.current_top_4
            ]
        else:
            rsrp_trend = [0.0, 0.0, 0.0, 0.0]
        # connected tower index (guaranteed to have a serving bs since generated reports >= 1)
        serving_one_hot = [0, 0, 0, 0]
        if self.agent.serving_bs in self.current_top_4:
            serving_position = self.current_top_4.index(self.agent.serving_bs)
            serving_one_hot[serving_position] = 1
        # speed (normalized to [0, 1], assuming max ~30 m/s)
        norm_speed = min(self.agent.speed / 30.0, 1.0)
        time_since_ho = self.agent.get_normalized_time_since_last_handover(
            self.agent.generated_reports[-1].timestep
        )
        obs = np.concatenate(
            [rsrp_list, rsrp_trend, serving_one_hot, [norm_speed], [time_since_ho]],
            dtype=np.float32,
        )
        return obs

    def step(self, action):
        # TODO: Increase CoolDown penalty, TODO 2: try deleting base penalty
        """Execute the action Handover/No Handover then move all the cars once"""
        # Reset() guarantees current_top_4 is populated
        target_bs = self.current_top_4[action]
        cooldown_penalty = 0.35

        # Dynamic penalty: higher if switching too soon after the last handover
        current_fcd = self.fcd_data[self.steps]
        current_timestep = (
            current_fcd[self.agent.id].timestep if self.agent.id in current_fcd else 0.0
        )
        time_since_ho = self.agent.get_normalized_time_since_last_handover(
            current_timestep
        )
        # time_since_ho is 0..1 where 0 = just switched, 1 = fully cooled down
        handover_penalty = cooldown_penalty * (1 - time_since_ho)

        current_fcd_dict: dict[int, CarFcdData] = self.fcd_data[self.steps]
        if self.agent.id not in current_fcd_dict:
            # The car reached its destination, Episode terminated.
            obs = self._get_obs()
            total_timesteps = len(self.fcd_data)
            return (
                obs,
                0.0,
                True,
                False,
                {
                    "timestep": self.steps,
                    "total_timesteps": total_timesteps,
                    "info": "Agent vehicle reached destination.",
                }, 
            )

        timestep = current_fcd_dict[self.agent.id].timestep
        tower_before_action = self.agent.serving_bs
        handover_executed = False
        # --- Execute Action ---
        if self.agent.serving_bs != target_bs:
            if self.agent.serving_bs is None:
                self.agent.connect_to_tower(bs=target_bs, timestep=timestep)
            else:
                # Trigger Handover For the agent only.
                self.agent.handover(target_bs, timestep=timestep)
                handover_executed = True

        # Move agent to next position
        total_timesteps = len(self.fcd_data)
        if self.steps < total_timesteps and self.agent.id in self.fcd_data[self.steps]:
            fcd = self.fcd_data[self.steps][self.agent.id]
            self.agent.move_to(
                fcd.latlng,
                timestep=fcd.timestep,
                speed=fcd.speed,
                angle=fcd.angle,
            )

        # Agent will have a new serving_bs ONLY if the action was to handover: [self.agent.handover(target_bs, timestep=timestep)]
        """
        The handover() func inside the UE explicitly says:         
        if self.serving_bs == target_bs:
            return
        """
        tower_after_action = self.agent.serving_bs

        report_after = self.agent.generated_reports[-1]

        # --- Update Top 4 ---
        self.current_top_4 = self._top_4_towers(report_after)

        # --- Calculate Reward ---
        if len(self.agent.generated_reports) >= 2 and tower_before_action is not None:
            report_before = self.agent.generated_reports[-2]

            if handover_executed:
                rsrp_stayed = WaveUtils.normalize_rsrp_index(
                    report_after.rsrp_values.get(tower_before_action.id, 0),
                    tower_before_action.radio,
                )
                rsrp_current = WaveUtils.normalize_rsrp_index(
                    report_after.rsrp_values.get(tower_after_action.id, 0),
                    tower_after_action.radio,
                )
                delta_rsrp = rsrp_current - rsrp_stayed
                reward = delta_rsrp - handover_penalty
            else:
                # Counterfactual: how does serving compare to the best alternative right now?
                serving_rsrp = WaveUtils.normalize_rsrp_index(
                    report_after.rsrp_values.get(tower_before_action.id, 0),
                    tower_before_action.radio,
                )
                best_alt_rsrp = max(
                    WaveUtils.normalize_rsrp_index(
                        report_after.rsrp_values.get(bs.id, 0), bs.radio
                    )
                    for bs in self.current_top_4
                    if bs != tower_before_action
                )
                # Positive if serving is stronger, negative if a better option exists
                reward = serving_rsrp - best_alt_rsrp

        else:
            reward = 0.0

        # --- RLF Penalty ---
        if tower_after_action is not None:
            serving_rsrp = WaveUtils.normalize_rsrp_index(
                report_after.rsrp_values.get(tower_after_action.id, 0),
                tower_after_action.radio,
            )
            rlf_threshold = UserEquipment.rlf_threshold.get(
                tower_after_action.radio, 25 / 97
            )
            if serving_rsrp < rlf_threshold:
                reward -= 1.5

        self.steps += 1
        truncated = self.steps >= total_timesteps  # End of the SUMO trace
        terminated = False
        info = {
            "timestep": self.steps,
            "total_timesteps": total_timesteps,
        }
        obs = self._get_obs()

        return obs, float(reward), terminated, truncated, info

    def reset(self, seed=None, options=None):
        super().reset(seed=seed, options=options)
        self.steps = 0
        WaveUtils.reset_fading_state()

        # Generate new randomized traffic scenario, retry if agent car 0 has too few timesteps
        min_agent_steps = 10
        while True:
            PathGeneration.quick_run(
                skip_netconvert=True,
                step_length=self.step_len,
                simulation_time=self.simulation_time,
            )
            self.fcd_data = FcdParser.parse_fcd_trace()
            agent_steps = sum(1 for frame in self.fcd_data if 0 in frame)
            if agent_steps >= min_agent_steps:
                break
        # Reinitialize agent (only UE needed since RSRP is independent of other cars)
        self.agent = UserEquipment(
            id=0,
            all_bs=self.base_towers,
            print_logs_on_movement=False,
            handover_algorithm=HandoverAlgorithm.NONE,
        )

        # Move agent to starting position (timestep 0)
        if len(self.fcd_data) > 0 and 0 in self.fcd_data[0]:
            fcd = self.fcd_data[0][0]
            self.agent.move_to(
                fcd.latlng,
                timestep=fcd.timestep,
                speed=fcd.speed,
                angle=fcd.angle,
            )

        # Step 0 consumed by the initial moves above
        self.steps = 1

        if self.agent.generated_reports:
            self.current_top_4 = self._top_4_towers(self.agent.generated_reports[-1])
        else:
            self.current_top_4 = self.base_towers[:4]

        obs = self._get_obs()
        info = {}
        return obs, info
